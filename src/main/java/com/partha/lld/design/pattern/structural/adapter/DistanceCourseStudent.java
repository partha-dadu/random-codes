package com.partha.lld.design.pattern.structural.adapter;

public class DistanceCourseStudent {
    private String name;
    private String regdNumber;
    private String email;
    private String university;
    private String course;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegdNumber() {
        return regdNumber;
    }

    public void setRegdNumber(String regdNumber) {
        this.regdNumber = regdNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUniversity() {
        return university;
    }

    public void setUniversity(String university) {
        this.university = university;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
